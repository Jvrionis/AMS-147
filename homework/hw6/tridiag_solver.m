function x = tridiag_solver(e,a,c,b)
%-------------------------------------------------------------%
%   Program written using GNU Octave, compatable with MATLAB  %
%   James Vrionis                                             %
%   tridiag_solver.m                                          %
%                                                             %
%  Use Thomas algorithm to solve tridiagonal linear           %
%  systems of equations                                       %
%          --------------------------------------             %
% Inputs:                                                     %
%        e - [ e_1 e_2 ... e_n-1]                             %
%        a - [a_1 a_2 ... a_n]                                %
%        c - [c_1 c_2 ... c_n-1]                              %
%        b - [b_1 b_2 ... b_n-1]^T                            %
%                                                             %
% Output:
%        x - [x_1 x_2 ... x_n]^T                              %
%-------------------------------------------------------------%

%-------------------------------------------------------------%
n=length(a);
m=zeros(n,n);
for i=1:n
    m(i,i)= a(i);
end
for k=1:length(e)
    m(k+1,k)=e(k);
end
for j=1:length(c)
   m(j,j+1)=c(j);
end
%-------------------------------------------------------------%

%-------------------------------------------------------------%
alpha(1)=a(1);
beta(1)=0;
for i=2:n
    beta(i)=e(i-1)/alpha(i-1);
    alpha(i)=a(i)-beta(i)*c(i-1);
end
%-------------------------------------------------------------%

%-------------------------------------------------------------%
y=zeros(n,1);
y(1)=b(1);
for i=2:n
    y(i)=b(i)-beta(i)*y(i-1);
end
%-------------------------------------------------------------%

%-------------------------------------------------------------%
x=zeros(n,1);
x(n)=y(n)/alpha(n);
for i= 1:(n-1)
    j=n-i;
    x(j)=(1/alpha(j))*(y(j)-c(j)*x(j+1));
end

end
%-------------------------------------------------------------%

